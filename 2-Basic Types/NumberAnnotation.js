/*
All numbers in TypeScript are either floating-point values or big integers.
The floating-point numbers have the type number while the big integers get the type bigint.
*/
// Number annotation
var numberValue;
numberValue = 100;
console.log(numberValue);
// declare a variable that holds a floating-point value
var price;
price = 10.5;
console.log(price);
// decimal numbers
var counter = 0;
var x = 100, y = 200;
console.log(x);
console.log(y);
// Binary Numbers
var bin = 4;
var anotherBin = 2;
console.log(bin);
console.log(anotherBin);
// Octal Numbers
var octal = 8;
console.log(octal);
// Hexadecimal numbers
var hexadecimal = 0xa;
console.log(hexadecimal);
